import gradio as gr
import numpy as np
from PIL import Image
from tensorflow.keras.models import load_model
from tensorflow.keras.preprocessing.image import img_to_array

# Load model
model_path = "/content/drive/MyDrive/waste_classifier.keras"
model = load_model(model_path)

# Labels and tips
class_names = ['Biodegradable', 'Non-Biodegradable']
tips = {
    'Biodegradable': "ğŸŒ¿ Compost organic waste like food scraps and leaves. Keep plastics out.",
    'Non-Biodegradable': "ğŸ” Recycle plastic, glass, and metal. Dispose e-waste at proper centers."
}

# Prediction function
def predict(image):
    image = image.convert("RGB").resize((224, 224))
    img_array = img_to_array(image) / 255.0
    img_array = np.expand_dims(img_array, axis=0)

    preds = model.predict(img_array)[0]
    label = class_names[np.argmax(preds)]
    confidence = float(np.max(preds))
    tip = tips[label]
    result_text = f"ğŸ§¾ **Prediction:** {label} \nğŸ“Š **Confidence:** {confidence * 100:.2f}%"
    return result_text, tip

